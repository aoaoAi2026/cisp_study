import os

def touch(day, lines):
    f = f'day-{day}.md'
    old = open(f, 'r', encoding='utf-8').read()
    new = old.rstrip('\n') + '\n\n' + '\n'.join(lines) + '\n'
    open(f, 'w', encoding='utf-8').write(new)
    print(f'Day {day}: {len(new.split(chr(10)))} lines')

