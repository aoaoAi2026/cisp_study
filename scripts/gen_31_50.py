#!/usr/bin/env python3
"""Generate Days 31-50 with 500+ line rich content."""
import os
OUT = '.'
phase_map = lambda d: '第一阶段 - 初级蓝队夯实' if d<=60 else ('第二阶段 - 中级蓝队进阶' if d<=90 else '第三阶段 - 高级蓝队升华')

def write_md(day, lines):
    f = os.path.join(OUT, f'day-{day}.md')
    content = '\n'.join(lines) + '\n'
    with open(f, 'w', encoding='utf-8') as fh:
        fh.write(content)
    return len(lines)

def gen_day(day, title, diff, goals, sections, tasks, checklist, interview, case, summary):
    p = phase_map(day)
    out = ['---', f'day: {day}', f'title: {title}', f'phase: {p}', f'difficulty: {diff}', '---', '',
           f'# Day {day}：{title}', '',
           f'> **阶段**：{p} | **难度**：{diff} | **课时**：2-3小时', '', '---', '',
           '## 📋 今日学习目标', '']
    for i, g in enumerate(goals, 1):
        out.append(f'{i}. {g}')
    out.extend(['', '---', '', '## 📖 核心知识讲解', ''])
    for idx, (sn, sb) in enumerate(sections, 1):
        cn = ['一','二','三','四','五','六','七'][idx-1] if idx<=7 else str(idx)
        out.append(f'### {cn}、{sn}')
        out.append('')
        out.extend(sb)
        out.extend(['', '---', ''])
    out.extend(['## 🔧 实操任务', ''])
    for i, t in enumerate(tasks, 1): out.append(f'{i}. {t}')
    out.extend(['', '---', '', '## ✅ 验收标准', ''])
    for c in checklist: out.append(f'- [ ] {c}')
    if interview:
        out.extend(['', '---', '', '## 💡 面试高频题', ''])
        for q, a in interview:
            out.append(f'**Q: {q}**')
            out.append('')
            out.append(f'A: {a}')
            out.append('')
    if case:
        out.extend(['', '---', '', '## 📊 真实案例', ''])
        out.extend(case)
    out.extend(['', '---', '', '## 📝 今日小结', ''])
    out.append(summary)
    out.extend(['', '---', '', '## 📚 延伸阅读',
                '1. 将今天所学整理到个人笔记库，用自己的话重写核心概念',
                '2. 搜索今天主题相关的CVE编号，在NVD上阅读漏洞详情',
                '3. 在OWASP官网阅读今天涉及的漏洞类型防护指南',
                '4. 搜索今天主题相关的真实攻防案例文章',
                '5. 在Blue Team Labs Online找一道相关练习',
                '6. 将今天学到的检测规则和命令添加到个人的命令速查手册',
                '7. 至少读一篇关于今天主题的行业分析报告',
                '8. 将今天遇到的疑问记录下来，作为明日学习的补充目标',
                ''])
    return out

# Helper: build section body from paragraphs
def S(*paragraphs):
    return list(paragraphs)

