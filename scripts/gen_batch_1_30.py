# Common supplement sections used across days
def make_supplement(day, extra_sections):
    """Build supplement lines list from extra_sections dict."""
    lines = []
    lines.append('---')
    lines.append('')
    lines.append('## 📖 深度补充内容')
    lines.append('')
    for title, body_lines in extra_sections:
        lines.append(f'### {title}')
        lines.append('')
        if isinstance(body_lines, list):
            lines.extend(body_lines)
        else:
            lines.append(body_lines)
        lines.append('')
    return lines

