import os

OUT = '.'

# ───── Template engine: build lines list, join, write ─────
def L(*args):
    """Collect lines into a flat list. Each arg is str or list of str."""
    out = []
    for a in args:
        if isinstance(a, list):
            out.extend(a)
        else:
            out.append(a)
    return out

def H(header, rows):
    """Markdown table from header list and rows list-of-lists."""
    lines = ['| ' + ' | '.join(header) + ' |',
             '| ' + ' | '.join(['---'] * len(header)) + ' |']
    for r in rows:
        lines.append('| ' + ' | '.join(str(c) for c in r) + ' |')
    return '\n'.join(lines)

def write_md(day, lines_list):
    fname = os.path.join(OUT, f'day-{day}.md')
    with open(fname, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines_list) + '\n')
    return len(lines_list)

def read_existing(day):
    fname = os.path.join(OUT, f'day-{day}.md')
    if not os.path.exists(fname):
        return None, 0
    with open(fname, 'r', encoding='utf-8') as f:
        content = f.read()
    lines = content.split('\n')
    return lines, len(lines)

def append_to_file(day, extra_lines):
    """Append extra_lines to existing file. Returns new total lines."""
    fname = os.path.join(OUT, f'day-{day}.md')
    if not os.path.exists(fname):
        return 0
    with open(fname, 'r', encoding='utf-8') as f:
        content = f.read()
    extra_str = '\n'.join(extra_lines)
    new_content = content.rstrip('\n') + '\n\n' + extra_str + '\n'
    with open(fname, 'w', encoding='utf-8') as f:
        f.write(new_content)
    return len(new_content.split('\n'))

