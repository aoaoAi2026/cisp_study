def gen_full_day(day, title, phase_str, difficulty, goals, sections, tasks, checklist_items, summary_text, case_text=None, interview_qa_data=None):
    """Generate a full 500+ line markdown file for a given day."""
    out = []
    # Frontmatter
    out.extend(['---', f'day: {day}', f'title: {title}', f'phase: {phase_str}', f'difficulty: {difficulty}', '---', ''])
    # Title
    out.extend([f'# Day {day}：{title}', '', f'> **阶段**：{phase_str} | **难度**：{difficulty} | **课时**：2-3小时', '', '---', ''])
    # Goals
    out.append('## 📋 今日学习目标')
    out.append('')
    for i, g in enumerate(goals, 1):
        out.append(f'{i}. {g}')
    out.extend(['', '---', ''])
    # Core knowledge sections
    out.append('## 📖 核心知识讲解')
    out.append('')
    for idx, sec in enumerate(sections, 1):
        sec_title, sec_body = sec[0], sec[1]
        out.append(f'### {idx}、{sec_title}')
        out.append('')
        if isinstance(sec_body, list):
            out.extend(sec_body)
        else:
            out.append(sec_body)
        out.extend(['', '---', ''])
    # Tasks
    out.append('## 🔧 实操任务')
    out.append('')
    for i, t in enumerate(tasks, 1):
        out.append(f'{i}. {t}')
    out.extend(['', '---', ''])
    # Checklist
    out.append('## ✅ 验收标准')
    out.append('')
    for c in checklist_items:
        out.append(f'- [ ] {c}')
    out.extend(['', '---', ''])
    # Interview Q&A
    if interview_qa_data:
        out.append('## 💡 面试高频题')
        out.append('')
        for q, a in interview_qa_data:
            out.append(f'**Q: {q}**')
            out.append(f'A: {a}')
            out.append('')
    # Case Study
    if case_text:
        out.append('## 📊 真实案例')
        out.append('')
        if isinstance(case_text, list):
            out.extend(case_text)
        else:
            out.append(case_text)
    # Summary
    out.append('')
    out.append('---')
    out.append('')
    out.append('## 📝 今日小结')
    out.append('')
    out.append(summary_text)
    out.extend(['', '---', ''])
    # Reading
    out.append('## 📚 延伸阅读')
    out.append('')
    out.append('1. 将今天所学整理到个人笔记库，用你自己的话重写核心概念')
    out.append('2. 在VirusTotal/微步在线中搜索今天涉及的攻击关键词，看真实案例')
    out.append('3. 搜索「ATT&CK for Cloud」了解云环境的ATT&CK映射')
    out.append('4. 阅读OWASP官方关于SQL注入的防护指南')
    out.append('5. 在Blue Team Labs Online找一道相关练习')
    out.append('6. 整理今天的关键命令和检测规则到个人的「命令速查手册」')
    out.append('7. 至少读一篇关于今天主题的真实攻防案例文章')
    out.append('8. 将今天遇到的不理解的概念记录下来，作为明日学习的补充目标')
    out.append('')
    return out

