def main():
    # 合并所有数据
    all_days = merge_days()

    # 添加第二阶段61-70天
    for day, (title, difficulty, content) in PHASE2_DAYS.items():
        all_days[day] = (title, difficulty, content)

    # 添加71-120天
    remaining = generate_remaining_days()
    all_days.update(remaining)

    # 生成文件
    created = 0
    for day in range(30, 121):
        if day not in all_days:
            print(f"Warning: Day {day} not defined, skipping...")
            continue

        title, difficulty, content_body = all_days[day]
        file_content = generate_file_content(day, title, difficulty, content_body)

        filepath = os.path.join(OUTPUT_DIR, f"day-{day}.md")
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(file_content)

        created += 1
        print(f"Created: day-{day}.md - {title}")

    print(f"\nTotal files created: {created} (Day 30-120)")

if __name__ == "__main__":
    main()
