export async function getCategories(): Promise<{ id: string; name: string; icon: string; count: number }[]> {
  const categoryMap = new Map<string, number>();
  
  allResources.forEach(resource => {
    const current = categoryMap.get(resource.category) || 0;
    categoryMap.set(resource.category, current + 1);
  });

  const categoryNames: Record<string, string> = {
    'penetration': '渗透测试',
    'vuln': '漏洞分析',
    'codeaudit': '代码审计',
    'djbh': '等级保护',
    'ctf': 'CTF竞赛',
    'cloud': '云安全',
    'mobile': '移动安全',
    'reverse': '逆向工程',
    'data-security': '数据安全',
    'incident': '应急响应',
    'intel': '威胁情报',
    'soc': '安全运营',
    'tools': '工具指南',
    'knowledge': '基础知识',
    'ai-security': 'AI安全',
    'supply-chain': '供应链安全',
    'ics-iot': 'ICS/IoT安全',
    'web3': 'Web3安全',
    'v2x': '车联网安全',
    'zero-trust': '零信任',
    'crypto-compliance': '密码合规',
    'hw': '护网行动',
  };

  return Array.from(categoryMap.entries()).map(([id, count]) => ({
    id,
    name: categoryNames[id] || id,
    icon: '',
    count
  }));
}

export function getStats(resources: Resource[]) {
  return {
    total: resources.length,
    categories: new Set(resources.map(r => r.category)).size,
    beginner: resources.filter(r => r.difficulty === '入门').length,
    intermediate: resources.filter(r => r.difficulty === '进阶').length,
    advanced: resources.filter(r => r.difficulty === '精通').length,
  };
}

export async function loadMarkdownContent(contentPath: string): Promise<string> {
  try {
    const response = await fetch(`/${contentPath}`);
    if (!response.ok) {
      return `# 内容加载失败\n\n无法加载文件: ${contentPath}`;
    }
    return await response.text();
  } catch {
    return `# 内容加载失败\n\n无法加载文件: ${contentPath}`;
  }
}