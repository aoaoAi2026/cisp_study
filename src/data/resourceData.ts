/** 异步加载全部资源 */
export async function loadAllResources(): Promise<Resource[]> {
  return allResources;
}

/** 根据 ID 获取单个资源 */
export async function getResourceById(id: string): Promise<Resource | undefined> {
  return allResources.find(r => r.id === id);
}

/** 获取所有分类统计 */
export function getCategories(): { key: string; name: string; count: number }[] {
  const countMap = new Map<string, number>();
  allResources.forEach(r => {
    countMap.set(r.category, (countMap.get(r.category) ?? 0) + 1);
  });
  return Array.from(countMap.entries()).map(([key, count]) => ({
    key,
    name: key,
    count,
  }));
}

/** 获取统计信息 */
export function getStats(): { total: number; categories: number; difficulty: Record<string, number> } {
  const categories = new Set<string>();
  const diff: Record<string, number> = {};
  allResources.forEach(r => {
    categories.add(r.category);
    diff[r.difficulty] = (diff[r.difficulty] ?? 0) + 1;
  });
  return { total: allResources.length, categories: categories.size, difficulty: diff };
}

/** 根据 contentPath 加载 Markdown 内容 */
export async function loadMarkdownContent(contentPath: string): Promise<string> {
  const response = await fetch(`/contents/${contentPath}`);
  if (!response.ok) {
    throw new Error(`Failed to load content: ${contentPath}`);
  }
  return response.text();
}